@csrf_exempt
def account(request, pk):
    try:
        user = User.objects.get(pk=pk)
        if request.method == 'GET':
            serializer = UserAndProfileUpdateSerializer(user)
            return JsonResponse(serializer.data)

        elif request.method == 'PATCH':
            data = JSONParser().parse(request)
            serializer = UserAndProfileUpdateSerializer(user, data=data, partial=True)
            if serializer.is_valid():
                serializer.save()
                if not serializer.data.get("is_active"):
                    activateEmail(request, serializer.data)
                return JsonResponse(serializer.data)
            return JsonResponse(serializer.errors, status=400)
        else:
            return JsonResponse({'Bad Request': 'The server could not understand the request due to invalid syntax.'},
                                status=400)
    except User.DoesNotExist:
        return JsonResponse({"error": "User does not exist"}, status=404)


