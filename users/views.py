@csrf_exempt
@api_view(['POST'])
def user_login(request):
    if request.method == 'POST':
        data = request.data['SignInData']
        serializer = UserLoginSerializer(data=data)
        if serializer.is_valid():
            username_or_email = serializer.initial_data['username_or_email']
            password = serializer.initial_data['password']
            user = authenticate(request, username=username_or_email, password=password)
            if user is not None:
                if user.is_active:
                    login(request, user)
                    token = Token.objects.get_or_create(user=user)
                    response_data = {'message': 'Logged in successfully.', 'Token': token[0].key, 'id': user.id}
                    response = JsonResponse(response_data, status=200)
                    return response
                else:
                    return activateEmail(request, serializer.data)
            else:
                return JsonResponse({'message': 'Invalid credentials.'}, status=401)
        else:
            return JsonResponse({'message': 'Invalid credentials.', "errors": f"{serializer.errors}"}, status=401)
    else:
        return JsonResponse({'Bad Request': 'The server could not understand the request due to invalid syntax.'},
                            status=400)


@csrf_exempt
@api_view(['POST'])
def user_list(request):
    if request.method == 'POST':
        data = request.data
        data['date_of_birth'] = data['date_of_birth'].split('T')[0]
        serializer = UserCreateUpdateSerializer(data=data)
        if serializer.is_valid():
            serializer.save()
            activateEmail(request, serializer.data)
